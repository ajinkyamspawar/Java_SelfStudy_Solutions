package SelfStudy;

public class Arithmetic 
{
	public static void main(String[] args) 
	{
		int i=10, j=20;
		
		System.out.println("Major arithmetic operations are as below:");
		System.out.println("Addition of "+i+","+j+" is "+(i+j));
		System.out.println("Subtraction of "+i+","+j+" is "+(i-j));
		System.out.println("Multiplication of "+i+","+j+" is "+(i*j));
		System.out.println("Division of "+j+","+i+" is "+(j/i));
	}

}

/* output is 
Major arithmetic operations are as below:
Addition of 10,20 is 30
Subtraction of 10,20 is -10
Multiplication of 10,20 is 200
Division of 20,10 is 2 */

